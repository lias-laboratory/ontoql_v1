package fr.ensma.lisi.ontoql.engine;

import java.io.InputStream;
import java.io.Reader;

import org.hibernate.QueryException;

import fr.ensma.lisi.ontoql.engine.antlr.SPARQLBaseLexer;

/**
 * Custom lexer for the SPARQL grammar. Extends the base lexer generated by
 * ANTLR in order to keep the grammar source file clean.
 * 
 * @author St√©phane JEAN
 */
class SPARQLLexer extends SPARQLBaseLexer {

	/**
	 * Constructor with an InputStream.
	 * 
	 * @param in an input InputStream
	 */
	public SPARQLLexer(final InputStream in) {
		super(in);
	}

	/**
	 * Constructor with a Reader.
	 * 
	 * @param in input Reader
	 */
	public SPARQLLexer(final Reader in) {
		super(in);
	}

	@Override
	public void panic() {
		panic("CharScanner: panic");
	}

	@Override
	public void panic(final String s) {
		// overriden to avoid System.exit
		throw new QueryException(s);
	}
}
